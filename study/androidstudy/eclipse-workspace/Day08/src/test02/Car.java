package test02;

public class Car {
	String color;
	int speed;
	int gear;
	
	public String getColor() {
		return color;
	}
	public int getSpeed() {
		return speed;
	}
	public int getGear() {
		return gear;
	}
	public void setColor(String color) {
		this.color = color;
	}
	public void setSpeed(int speed) {
		this.speed=speed;
	}
	public void setGear(int gear) {
		this.gear=gear;
	}
}
